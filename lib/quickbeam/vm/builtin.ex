  @doc "Emits fallback clauses for modules that define prototype or static properties."
  defmacro __before_compile__(env) do
    has_proto = Module.get_attribute(env.module, :__has_proto)
    has_static = Module.get_attribute(env.module, :__has_static)

    static_names =
      Module.get_attribute(env.module, :__static_names) |> Enum.reverse() |> Enum.uniq()

    proto_fallback =
      if has_proto do
        quote do
          def proto_property(_), do: :undefined
          def proto_property_meta(_), do: nil
          def proto_property_spec(_), do: nil
        end
      end

    static_fallback =
      if has_static do
        quote do
          def static_property(_), do: :undefined
          def static_property_meta(_), do: nil
          def static_property_spec(_), do: nil
        end
      end

    static_names_fun =
      if has_static do
        quote do
          def static_property_names, do: unquote(Macro.escape(static_names))
        end
      end

    [proto_fallback, static_fallback, static_names_fun]
    |> Enum.reject(&is_nil/1)
    |> case do
      [] -> nil
      blocks -> {:__block__, [], blocks}
    end
  end

