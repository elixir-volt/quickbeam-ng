         |> QuickBEAM.VM.ObjectModel.WrappedPrimitive.value(:symbol) do
      {:ok, value} ->
        value

      :error ->
        QuickBEAM.VM.JSThrow.type_error!(
          "Symbol.prototype method called on incompatible receiver"
        )
    end
  end

  defp require_symbol_receiver!(_value),
    do:
      QuickBEAM.VM.JSThrow.type_error!("Symbol.prototype method called on incompatible receiver")

  def iterator_from(items) do
    iter = QuickBEAM.VM.Heap.wrap_iterator(items)

    with {:obj, ref} <- iter do
      QuickBEAM.VM.Heap.update_obj(ref, %{}, fn map ->
        Map.put(
          map,
          {:symbol, "Symbol.iterator"},
          builtin("[Symbol.iterator]", fn _, this -> this end)
        )
      end)
    end

    iter
  end

  # ── Runtime dispatch ──

  import QuickBEAM.VM.Heap.Keys, only: [proxy_target: 0]
  alias QuickBEAM.VM.{Heap, JSThrow}

  @doc "Dispatches a VM callable value to its underlying Elixir callback."
  def call({:builtin, name, _cb}, _args, _this)
      when name in ~w(Map Set WeakMap WeakSet WeakRef FinalizationRegistry DataView Promise) do
    JSThrow.type_error!("Constructor #{name} requires 'new'")
  end

  def call({:builtin, _, cb}, args, this) when is_function(cb, 2), do: cb.(args, this)

  def call({:bound, _, inner, _, _}, args, this), do: call(inner, args, this)

  def call({:obj, _} = obj, _args, _this) do
    if obj == Heap.get_func_proto(), do: :undefined, else: JSThrow.type_error!("not a function")
  end

  def call(f, args, _this) when is_function(f, 2), do: f.(args, nil)

  def call(f, args, _this) when is_function(f, 1), do: f.(args)

  def call(f, args, _this) when is_function(f), do: apply(f, args)

  def call(_, _, _),
    do: JSThrow.type_error!("not a function")

  @doc "Returns a built-in function's JavaScript name, if present."
  def name({:builtin, name, _}), do: name
  def name(_), do: nil

  @doc "Returns whether a VM value can be called as a JavaScript function."
  def callable?(%QuickBEAM.VM.Function{}), do: true

  def callable?({:closure, _, %QuickBEAM.VM.Function{}}), do: true

  def callable?({:builtin, _, callback}) when is_function(callback), do: true
  def callable?({:builtin, _, _}), do: false

  def callable?({:bound, _, _, _, _}), do: true

  def callable?({:obj, ref} = obj) do
    obj == Heap.get_func_proto() or
      case Heap.get_obj(ref, %{}) do
        %{proxy_target() => target} -> callable?(target)
        _ -> false
      end
  end

  def callable?(f) when is_function(f), do: true

  def callable?(_), do: false
end
