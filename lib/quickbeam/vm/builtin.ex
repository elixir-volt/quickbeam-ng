defmodule QuickBEAM.VM.Builtin do
  @moduledoc """
  Macros and helpers for defining JS builtins in all contexts.

  All builtins use 2-arity `fn args, this ->` convention.

  ## Module-level dispatch (generates def clauses)

      proto "push" do ... end         # → def proto_property("push")
      static "isArray" do ... end     # → def static_property("isArray")
      constant "PI", :math.pi()      # → def static_property("PI"), do: value

  ## Named object (generates def object/0)

      js_object "Math" do
        method "floor" do ... end
        val "PI", :math.pi()
      end

  ## Inline maps and objects

      build_methods do              # returns %{"name" => {:builtin, ...}, ...}
        method "add" do ... end
        args_method "from", &from_args/1
        receiver_method "slice", &slice/2
        this_method "toJSON", &to_json/1
        symbol :iterator do
          method do ... end
        end
        accessor "size" do
          get do ... end
        end
        prop "size", 0
      end

      object do                     # returns Heap.wrap(%{"name" => {:builtin, ...}, ...})
        prop "type", type
        method "preventDefault" do ... end
      end

      object heap: false do         # returns the raw map for post-processing
        prop "searchParams", search_params
      end

      constructor "DOMException", &build_dom_exception/2 do
        proto do
          extends build_error_proto()
        end
      end

  `args` and `this` are injected in all `proto`/`static`/`method` bodies.
  `arg/3`, `argv/2`, `builtin_*`, and `iterator_from/1` cover the repeated
  argument-defaulting, function-wrapping, and iterator boilerplate used by Web APIs.
  Catch-all fallbacks are auto-generated by @before_compile.
  """

  alias QuickBEAM.VM.Heap
  alias QuickBEAM.VM.ObjectModel.PropertyDescriptor

  defmodule FunctionSpec do
    @moduledoc "Declarative metadata for an ECMAScript builtin function."

    defstruct name: nil,
              length: 0,
              kind: :function,
              constructable?: false,
              writable?: true,
              enumerable?: false,
              configurable?: true,
              ecma: nil,
              annex: nil
  end

  defmodule PropertySpec do
    @moduledoc "Declarative metadata for an ECMAScript builtin property."

    defstruct key: nil,
              kind: :data,
              value: nil,
              descriptor: %{},
              ecma: nil,
              annex: nil
  end

  defmodule ObjectSpec do
    @moduledoc "Declarative metadata for an ECMAScript builtin object."

    defstruct name: nil,
              intrinsic: nil,
              properties: [],
              prototype: nil,
              ecma: nil,
              annex: nil
  end

  defmodule IntrinsicSpec do
    @moduledoc "Declarative metadata for an ECMAScript intrinsic graph entry."

    defstruct name: nil,
              constructor: nil,
              statics: [],
              prototype: nil,
              definition: nil,
              ecma: nil,
              annex: nil
  end

  defmodule Meta do
    @moduledoc "Metadata attached to JavaScript builtin functions and properties."

    defstruct name: nil,
              length: 0,
              constructable?: false,
              writable?: true,
              enumerable?: false,
              configurable?: true,
              kind: :function,
              ecma: nil,
              annex: nil
  end

  @doc "Builds builtin metadata from macro options."
  def meta(name, opts \\ [], kind \\ :function) do
    name
    |> function_spec(opts, kind)
    |> function_meta()
  end

  def function_meta(%FunctionSpec{} = spec) do
    %Meta{
      name: spec.name,
      length: spec.length,
      constructable?: spec.constructable?,
      writable?: spec.writable?,
      enumerable?: spec.enumerable?,
      configurable?: spec.configurable?,
      kind: spec.kind,
      ecma: spec.ecma,
      annex: spec.annex
    }
  end

  @doc "Builds a declarative accessor property spec."
  def accessor_property_spec(key, getter, setter \\ nil, opts \\ []) do
    %PropertySpec{
      key: key,
      kind: :accessor,
      value: {:accessor, getter, setter},
      descriptor: %{
        enumerable: Keyword.get(opts, :enumerable, false),
        configurable: Keyword.get(opts, :configurable, true)
      },
      ecma: Keyword.get(opts, :ecma),
      annex: Keyword.get(opts, :annex)
    }
  end

  @doc "Builds a declarative data property spec."
  def data_property_spec(key, value, opts \\ []) do
    %PropertySpec{
      key: key,
      kind: :data,
      value: value,
      descriptor: descriptor_from_opts(opts),
      ecma: Keyword.get(opts, :ecma),
      annex: Keyword.get(opts, :annex)
    }
  end

  @doc "Builds a declarative property spec for a builtin function property."
  def property_spec(key, %FunctionSpec{} = function_spec) do
    %PropertySpec{
      key: key,
      kind: function_spec.kind,
      value: function_spec,
      descriptor: %{
        writable: function_spec.writable?,
        enumerable: function_spec.enumerable?,
        configurable: function_spec.configurable?
      },
      ecma: function_spec.ecma,
      annex: function_spec.annex
    }
  end

  defp descriptor_from_opts(opts) do
    %{
      writable: Keyword.get(opts, :writable, true),
      enumerable: Keyword.get(opts, :enumerable, false),
      configurable: Keyword.get(opts, :configurable, true)
    }
  end

  @doc "Builds a first-class function spec from builtin macro options."
  def function_spec(name, opts \\ [], kind \\ :function) do
    %FunctionSpec{
      name: Keyword.get(opts, :name, name),
      length: Keyword.get(opts, :length, 0),
      constructable?: Keyword.get(opts, :constructable, false),
      writable?: Keyword.get(opts, :writable, true),
      enumerable?: Keyword.get(opts, :enumerable, false),
      configurable?: Keyword.get(opts, :configurable, true),
      kind: kind,
      ecma: Keyword.get(opts, :ecma),
      annex: Keyword.get(opts, :annex)
    }
  end

  @doc "Builds an intrinsic spec for a runtime module and builtin definition."
  def intrinsic_spec(module, definition) when is_atom(module) do
    proto_specs = module_property_specs(module, :proto)
    static_specs = module_property_specs(module, :static)

    %IntrinsicSpec{
      name: definition.name,
      definition: definition,
      constructor:
        function_spec(definition.name,
          length: definition.length || 0,
          constructable: definition.constructable?,
          ecma: definition.ecma,
          annex: definition.annex
        ),
      statics: static_specs,
      prototype: %ObjectSpec{
        name: definition.name <> ".prototype",
        properties: proto_specs,
        ecma: definition.ecma,
        annex: definition.annex
      },
      ecma: definition.ecma,
      annex: definition.annex
    }
  end

  defp module_property_specs(module, kind) do
    names_fun = if kind == :proto, do: :proto_property_names, else: :static_property_names
    spec_fun = if kind == :proto, do: :proto_property_spec, else: :static_property_spec

    if function_exported?(module, names_fun, 0) do
      module
      |> apply(names_fun, [])
      |> Enum.map(fn name -> module |> apply(spec_fun, [name]) |> maybe_property_spec(name) end)
      |> Enum.reject(&is_nil/1)
    else
      []
    end
  end

  defp maybe_property_spec(nil, _key), do: nil
  defp maybe_property_spec(%FunctionSpec{} = spec, key), do: property_spec(key, spec)
  defp maybe_property_spec(%PropertySpec{} = spec, _key), do: spec

  @doc "Returns metadata for a module-level static builtin, when the module declares it."
  def static_meta(module, name) when is_atom(module) do
    if function_exported?(module, :static_property_meta, 1), do: module.static_property_meta(name)
  end

  @doc "Returns metadata for a module-level prototype builtin, when the module declares it."
  def proto_meta(module, name) when is_atom(module) do
    if function_exported?(module, :proto_property_meta, 1), do: module.proto_property_meta(name)
  end

  @doc "Returns whether builtin metadata marks a value as constructable."
  def constructable?(%Meta{constructable?: constructable?}), do: constructable?
  def constructable?(_), do: false

  @doc "Returns the declared length from builtin metadata or zero."
  def length(%Meta{length: length}), do: length
  def length(_), do: 0

  @doc "Returns metadata attached to a builtin function, falling back to built-in name metadata."
  def metadata_for({:builtin, name, _} = builtin) do
    Map.get(Heap.get_ctor_statics(builtin), :__builtin_meta__) || named_meta(name)
  end

  def metadata_for(name) when is_binary(name), do: named_meta(name)
  def metadata_for(_), do: nil

  @doc "Returns the declared builtin length or the supplied default."
  def declared_length(value, default \\ :undefined) do
    case metadata_for(value) do
      %Meta{length: length} -> length
      _ -> default
    end
  end

  @doc "Stores internal builtin metadata on a callable or constructor."
  def put_builtin_metadata(function, %Meta{} = meta) do
    Heap.put_ctor_static(function, :__builtin_meta__, meta)
    function
  end

  @doc "Stores JavaScript-visible function name and length metadata."
  def put_function_metadata(function, name, length) do
    Heap.put_ctor_static(function, "length", length)
    Heap.put_ctor_static(function, "name", name)
    function
  end

