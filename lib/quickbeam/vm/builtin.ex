  @doc "Builds builtin metadata from macro options."
  def meta(name, opts \\ [], kind \\ :function) do
    name
    |> function_spec(opts, kind)
    |> function_meta()
  end

  def function_meta(%FunctionSpec{} = spec) do
    %Meta{
      name: spec.name,
      length: spec.length,
      constructable?: spec.constructable?,
      writable?: spec.writable?,
      enumerable?: spec.enumerable?,
      configurable?: spec.configurable?,
      kind: spec.kind,
      ecma: spec.ecma,
      annex: spec.annex
    }
  end

  @doc "Builds a first-class function spec from builtin macro options."
  def function_spec(name, opts \\ [], kind \\ :function) do
    %FunctionSpec{
      name: Keyword.get(opts, :name, name),
      length: Keyword.get(opts, :length, 0),
      constructable?: Keyword.get(opts, :constructable, false),
      writable?: Keyword.get(opts, :writable, true),
      enumerable?: Keyword.get(opts, :enumerable, false),
      configurable?: Keyword.get(opts, :configurable, true),
      kind: kind,
      ecma: Keyword.get(opts, :ecma),
      annex: Keyword.get(opts, :annex)
    }
  end

  @doc "Returns metadata for a module-level static builtin, when the module declares it."
  def static_meta(module, name) when is_atom(module) do
    if function_exported?(module, :static_property_meta, 1), do: module.static_property_meta(name)
  end

  @doc "Returns metadata for a module-level prototype builtin, when the module declares it."
  def proto_meta(module, name) when is_atom(module) do
    if function_exported?(module, :proto_property_meta, 1), do: module.proto_property_meta(name)
  end

  @doc "Returns whether builtin metadata marks a value as constructable."
  def constructable?(%Meta{constructable?: constructable?}), do: constructable?
  def constructable?(_), do: false

  @doc "Returns the declared length from builtin metadata or zero."
  def length(%Meta{length: length}), do: length
  def length(_), do: 0

  @doc "Returns metadata attached to a builtin function, falling back to legacy name metadata."
  def metadata_for({:builtin, name, _} = builtin) do
    Map.get(Heap.get_ctor_statics(builtin), :__builtin_meta__) || named_meta(name)
  end

  def metadata_for(name) when is_binary(name), do: named_meta(name)
  def metadata_for(_), do: nil

  @doc "Returns the declared builtin length or the supplied default."
  def declared_length(value, default \\ :undefined) do
    case metadata_for(value) do
      %Meta{length: length} -> length
      _ -> default
    end
  end

  @doc "Stores internal builtin metadata on a callable or constructor."
  def put_builtin_metadata(function, %Meta{} = meta) do
    Heap.put_ctor_static(function, :__builtin_meta__, meta)
    function
  end

  @doc "Stores JavaScript-visible function name and length metadata."
  def put_function_metadata(function, name, length) do
    Heap.put_ctor_static(function, "length", length)
    Heap.put_ctor_static(function, "name", name)
    function
  end

