  defp build_builtin_definition(name, opts, caller) do
    definition = build_definition_struct(name, opts, caller)

    quote do
      def builtin_definition do
        unquote(definition)
      end

      def builtin_spec do
        QuickBEAM.VM.Builtin.intrinsic_spec(__MODULE__, builtin_definition())
      end

      @ecma nil
      @annex nil
    end
  end

  defp build_definition_struct(name, opts, caller) do
    constructor = Keyword.fetch!(opts, :constructor)
    length = Keyword.get(opts, :length)
    phase = Keyword.get(opts, :phase, :runtime)
    module = Keyword.get(opts, :module, caller.module)
    prototype_parent = Keyword.get(opts, :prototype_parent, :object)
    constructor_descriptor = Keyword.get(opts, :constructor_descriptor, %{})
    prototype_descriptor = Keyword.get(opts, :prototype_descriptor, %{})
    prototype_properties = Keyword.get(opts, :prototype_properties, [])
    constructable? = Keyword.get(opts, :constructable, true)
    intrinsic_key = Keyword.get(opts, :intrinsic_key)
    after_install = Keyword.get(opts, :after_install)
    auto_install? = Keyword.get(opts, :auto_install?, true)

    quote do
      builtin_attrs =
        QuickBEAM.VM.Builtin.opts_with_builtin_attrs(
          unquote(Macro.escape(opts)),
          @ecma,
          @annex
        )

      struct!(QuickBEAM.VM.Builtin.Definition, %{
        name: unquote(name),
        constructor: unquote(constructor),
        length: unquote(length),
        phase: unquote(phase),
        module: unquote(module),
        prototype_parent: unquote(prototype_parent),
        constructor_descriptor:
          Map.merge(
            unquote(Macro.escape(PropertyDescriptor.constructor())),
            unquote(Macro.escape(constructor_descriptor))
          ),
        prototype_descriptor:
          Map.merge(
            unquote(Macro.escape(PropertyDescriptor.prototype())),
            unquote(Macro.escape(prototype_descriptor))
          ),
        prototype_properties: unquote(prototype_properties),
        constructable?: unquote(constructable?),
        intrinsic_key: unquote(intrinsic_key),
        ecma: Keyword.get(builtin_attrs, :ecma),
        annex: Keyword.get(builtin_attrs, :annex),
        after_install: unquote(after_install),
        auto_install?: unquote(auto_install?)
      })
    end
  end

  defp build_contextual_methods(block, target) do
    block
    |> contextual_methods(target)
    |> block_from_entries()
  end

  defp parse_intrinsic_block(block) do
    block
    |> normalize_block()
    |> Enum.reduce({[], []}, fn
      {:install, _, [[do: body]]}, {opts, declarations} ->
        {append_after_install(opts, build_install_callback(body)), declarations}

      {:prototype, _, [prototype_opts, [do: body]]}, {opts, declarations}
      when is_list(prototype_opts) ->
        opts = append_after_install(opts, build_prototype_install_callback(prototype_opts, body))
        declarations = declarations ++ prototype_method_declarations(body)
        {opts, declarations}

      {:install_with, _, [callback]}, {opts, declarations} ->
        {append_after_install(opts, callback), declarations}

      {:phase, _, [phase]}, {opts, declarations} ->
        {Keyword.put(opts, :phase, phase), declarations}

      {:constructor, _, [constructor_opts, [do: body]]}, {opts, declarations}
      when is_list(constructor_opts) ->
        {Keyword.merge(
           opts,
           Keyword.put(constructor_opts, :constructor, build_constructor_callback(body))
         ), declarations}

      {:constructor, _, [[do: body]]}, {opts, declarations} ->
        {Keyword.put(opts, :constructor, build_constructor_callback(body)), declarations}

      {:constructor, _, [callback, constructor_opts]}, {opts, declarations}
      when is_list(constructor_opts) ->
        {Keyword.merge(opts, Keyword.put(constructor_opts, :constructor, callback)), declarations}

      {:constructor, _, [callback]}, {opts, declarations} ->
        {Keyword.put(opts, :constructor, callback), declarations}

      _, acc ->
        acc
    end)
  end

  defp append_after_install(opts, callback) do
    case Keyword.get(opts, :after_install) do
      nil ->
        Keyword.put(opts, :after_install, callback)

      existing ->
        Keyword.put(opts, :after_install, build_install_sequence(existing, callback))
    end
  end

  defp build_install_sequence(first, second) do
    quote do
      fn var!(ctor), var!(opts) ->
        QuickBEAM.VM.Builtin.run_install_callback(unquote(first), var!(ctor), var!(opts))
        QuickBEAM.VM.Builtin.run_install_callback(unquote(second), var!(ctor), var!(opts))
      end
    end
  end

  defp build_constructor_callback(body) do
    quote do
      fn var!(args), var!(this) ->
        _ = var!(args)
        _ = var!(this)
        unquote(body)
      end
    end
  end

  defp build_install_callback(body) do
    quote do
      fn var!(ctor), var!(opts) ->
        _ = var!(ctor)
        _ = var!(opts)
        unquote(body)
      end
    end
  end

  defp build_prototype_install_callback(prototype_opts, body) do
    install_body = prototype_install_body(body)

    quote do
      fn var!(ctor), var!(opts) ->
        _ = var!(ctor)
        _ = var!(opts)

        prototype unquote(Macro.escape(prototype_opts)) do
          unquote(install_body)
        end
      end
    end
  end

  defp prototype_install_body(body) do
    body
    |> normalize_block()
    |> Enum.reject(&prototype_declaration?/1)
    |> block_from_entries()
  end

  defp prototype_method_declarations(body) do
    entries = body |> normalize_block() |> Enum.filter(&prototype_declaration?/1)

    if entries == [] do
      []
    else
      [build_contextual_methods(block_from_entries(entries), :proto)]
    end
  end

  defp prototype_declaration?({:@, _, _}), do: true
  defp prototype_declaration?({:method, _, _}), do: true
  defp prototype_declaration?({:symbol, _, _}), do: true
  defp prototype_declaration?({:accessor, _, _}), do: true
  defp prototype_declaration?({:getter, _, _}), do: true
  defp prototype_declaration?({:property, _, _}), do: true
  defp prototype_declaration?(_entry), do: false

  defmacro symbol(_name, do: _block) do
    raise ArgumentError, "symbol is only available inside builtin object/spec blocks"
  end

  defp contextual_methods(block, target) do
    block
    |> normalize_block()
    |> Enum.map(fn
      {:@, _, _} = attr ->
        attr

      {:method, meta, [name, [do: body]]} ->
        {target, meta, [name, [do: body]]}

      {:method, meta, [name, opts, [do: body]]} ->
        {target, meta, [name, opts, [do: body]]}

      {:symbol, meta, [name, [do: symbol_block]]} ->
        contextual_symbol_entry(target, meta, name, symbol_block)

      {:property, meta, [name, opts]} when is_list(opts) ->
        value = Keyword.fetch!(opts, :value)
        opts = Keyword.delete(opts, :value)
        target_val = if target == :proto, do: :proto_data, else: :static_data
        {target_val, meta, [name, value, opts]}

      {:accessor, meta, [name, [do: block]]} ->
        target_accessor = if target == :proto, do: :proto_accessor, else: :static_accessor
        {target_accessor, meta, [name, [do: block]]}

      {:getter, meta, [name, [do: body]]} ->
        target_getter = if target == :proto, do: :proto_getter, else: :static_getter
        {target_getter, meta, [name, [do: body]]}

      other ->
        other
    end)
  end

  defp contextual_symbol_entry(target, meta, name, block) do
    key = symbol_key(name)

    case normalize_block(block) do
      [{:method, method_meta, [[do: body]]}] ->
        {target, method_meta, [key, [do: body]]}

      [{:method, method_meta, [opts, [do: body]]}] when is_list(opts) ->
        {target, method_meta, [key, opts, [do: body]]}

      entries when is_list(entries) ->
        if Enum.any?(entries, &match?({kind, _, _} when kind in [:get, :set], &1)) do
          target_accessor = if target == :proto, do: :proto_accessor, else: :static_accessor
          {target_accessor, meta, [key, [do: block]]}
        else
          raise ArgumentError, "symbol #{inspect(name)} must declare method/get/set"
        end
    end
  end

  defp symbol_key({:symbol, _} = key), do: key
  defp symbol_key(name) when is_atom(name), do: {:symbol, "Symbol.#{name}"}
  defp symbol_key("Symbol." <> _ = name), do: {:symbol, name}
  defp symbol_key(name) when is_binary(name), do: {:symbol, "Symbol.#{name}"}

  defp block_from_entries([]), do: :ok
  defp block_from_entries([entry]), do: entry
  defp block_from_entries(entries), do: {:__block__, [], entries}

