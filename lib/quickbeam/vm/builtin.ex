  defp build_builtin(name, body, opts) do
    callback_body = build_callback_body(body, Keyword.get(opts, :receiver))

    quote do
      QuickBEAM.VM.Builtin.builtin(
        unquote(builtin_property_name(name)),
        fn var!(args), var!(this) ->
          _ = var!(args)
          unquote(callback_body)
        end,
        unquote(Macro.escape(opts))
      )
    end
  end

  defp build_callback_body(body, nil) do
    quote do
      _ = var!(this)
      unquote(body)
    end
  end

  defp build_callback_body(body, receiver) do
    quote do
      var!(this) = QuickBEAM.VM.Builtin.require_receiver!(unquote(receiver), var!(this))
      unquote(body)
    end
  end

  defp normalize_block({:__block__, _, entries}), do: entries
  defp normalize_block(single), do: [single]

  defp build_map_entries(entries) do
    {map_entries, _pending_opts} =
      Enum.reduce(entries, {[], []}, fn
        {:@, _, [{:ecma, _, [ecma]}]}, {built, pending_opts} ->
          {built, Keyword.put(pending_opts, :ecma, ecma)}

        {:@, _, [{:annex, _, [annex]}]}, {built, pending_opts} ->
          {built, Keyword.put(pending_opts, :annex, annex)}

        {:@, _, [{:doc, _, [_doc]}]}, {built, pending_opts} ->
          {built, pending_opts}

        entry, {built, pending_opts} ->
          {[build_map_entry(entry, pending_opts) | built], []}
      end)

    Enum.reverse(map_entries)
  end

  defp parse_constructor_block(block) do
    block
    |> normalize_block()
    |> Enum.reduce({[], nil}, fn
      {:proto, _, [[do: proto_block]]}, {_entries, _parent} ->
        parse_proto_block(proto_block)

      entry, {entries, parent} ->
        {[entry | entries], parent}
    end)
    |> then(fn {entries, parent} -> {Enum.reverse(entries), parent} end)
  end

  defp parse_proto_block(block) do
    block
    |> normalize_block()
    |> Enum.reduce({[], nil}, fn
      {:extends, _, [parent]}, {entries, _parent} -> {entries, parent}
      entry, {entries, parent} -> {[entry | entries], parent}
    end)
    |> then(fn {entries, parent} -> {Enum.reverse(entries), parent} end)
  end

