  defp build_builtin_definition(name, opts, caller) do
    constructor = Keyword.fetch!(opts, :constructor)
    length = Keyword.get(opts, :length)
    phase = Keyword.get(opts, :phase, :runtime)
    module = Keyword.get(opts, :module, caller.module)
    prototype_parent = Keyword.get(opts, :prototype_parent, :object)
    constructor_descriptor = Keyword.get(opts, :constructor_descriptor, %{})
    prototype_descriptor = Keyword.get(opts, :prototype_descriptor, %{})
    prototype_properties = Keyword.get(opts, :prototype_properties, [])
    constructable? = Keyword.get(opts, :constructable, true)
    intrinsic_key = Keyword.get(opts, :intrinsic_key)
    after_install = Keyword.get(opts, :after_install)
    auto_install? = Keyword.get(opts, :auto_install?, true)

    quote do
      def builtin_definition do
        struct!(QuickBEAM.VM.Builtin.Definition, %{
          name: unquote(name),
          constructor: unquote(constructor),
          length: unquote(length),
          phase: unquote(phase),
          module: unquote(module),
          prototype_parent: unquote(prototype_parent),
          constructor_descriptor:
            Map.merge(
              unquote(Macro.escape(PropertyDescriptor.constructor())),
              unquote(Macro.escape(constructor_descriptor))
            ),
          prototype_descriptor:
            Map.merge(
              unquote(Macro.escape(PropertyDescriptor.prototype())),
              unquote(Macro.escape(prototype_descriptor))
            ),
          prototype_properties: unquote(prototype_properties),
          constructable?: unquote(constructable?),
          intrinsic_key: unquote(intrinsic_key),
          ecma:
            Keyword.get(
              QuickBEAM.VM.Builtin.opts_with_builtin_attrs(
                unquote(Macro.escape(opts)),
                @ecma,
                @annex
              ),
              :ecma
            ),
          annex:
            Keyword.get(
              QuickBEAM.VM.Builtin.opts_with_builtin_attrs(
                unquote(Macro.escape(opts)),
                @ecma,
                @annex
              ),
              :annex
            ),
          after_install: unquote(after_install),
          auto_install?: unquote(auto_install?)
        })
      end

      @ecma nil
      @annex nil
    end
  end

  defp build_contextual_methods(block, target) do
    block
    |> contextual_methods(target)
    |> block_from_entries()
  end

  defp parse_intrinsic_block(block) do
    block
    |> normalize_block()
    |> Enum.flat_map(fn
      {:install_with, _, [callback]} ->
        [after_install: callback]

      {:phase, _, [phase]} ->
        [phase: phase]

      {:constructor, _, [opts, [do: body]]} when is_list(opts) ->
        Keyword.put(opts, :constructor, build_constructor_callback(body))

      {:constructor, _, [[do: body]]} ->
        [constructor: build_constructor_callback(body)]

      {:constructor, _, [callback, opts]} when is_list(opts) ->
        Keyword.put(opts, :constructor, callback)

      {:constructor, _, [callback]} ->
        [constructor: callback]

      _ ->
        []
    end)
  end

  defp build_constructor_callback(body) do
    quote do
      fn var!(args), var!(this) ->
        _ = var!(args)
        _ = var!(this)
        unquote(body)
      end
    end
  end

  defp contextual_methods(block, target) do
    block
    |> normalize_block()
    |> Enum.map(fn
      {:@, _, _} = attr ->
        attr

      {:method, meta, [name, [do: body]]} ->
        {target, meta, [name, [do: body]]}

      {:method, meta, [name, opts, [do: body]]} ->
        {target, meta, [name, opts, [do: body]]}

      other ->
        other
    end)
  end

  defp block_from_entries([entry]), do: entry
  defp block_from_entries(entries), do: {:__block__, [], entries}

