  defp build_constructor(name, callback, proto_entries, parent) do
    proto_map_entries = build_map_entries(proto_entries)

    quote do
      QuickBEAM.VM.Runtime.ConstructorRegistry.register(
        unquote(name),
        unquote(callback),
        %{unquote_splicing(proto_map_entries)},
